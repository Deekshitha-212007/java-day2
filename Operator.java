public class Operator{
    public static void main(String[]args)
    {
        int a=13;
        int b=56;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        System.out.println("Relational Operator");
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        System.out.println("Unary Operator");
        System.out.println(a--);
        System.out.println(++b);
        System.out.println(--a);
        System.out.println(b++);
        System.out.println(-a);
        System.out.println(+b);
        System.out.println(-b);
        System.out.println(+a);

    }
}